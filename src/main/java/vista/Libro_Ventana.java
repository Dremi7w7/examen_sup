private void actulizarTabla1 () {
    
        String [] encabezado = new String[4];
        encabezado[0]="Categoria";
        encabezado[1]="Codigo";
        encabezado[2]="Nombre";
        encabezado[3]="A침o";
        
        var datos = new Object[this.libroControl.listar().size()][4];
        
        var i=0;
        for(var libro:this.libroControl.listar())
        {
            datos[i][0]=libro.getCategoria().getDescripcion();
            datos[i][1]=libro.getCodigo();
            datos[i][2]=libro.getNombre();
            datos[i][3]=libro.getYear();
            
            i++;
        }
        this.modeloTabla = new DefaultTableModel(datos,encabezado);
        this.jTable1.setModel(modeloTabla);
        
        
    } 
    
    
    
